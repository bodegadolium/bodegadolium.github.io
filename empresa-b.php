<h3>ORGULLOSOS DE SER UNA <strong>EMPRESA B</strong></h3>
<p>Dolium, primera bodega de Latinoamérica totalmente subterránea, fue diseñada para compatibilizar las antiguas tradiciones con la más moderna tecnología y producir vinos de alta calidad. 
Dolium es el nombre latino de las ánforas vinarias en las cuales los romanos fueron los primeros en conservar sus mejores vinos bajo tierra, en condiciones óptimas de temperatura.
 Dolium nace con el más alto concepto de ECOLOGIA incorporado. Nuestros vinos responden a un perfil internacional manteniendo la personalidad de su terruño natal. Es un compromiso hacia el consumidor y con nosotros mismos.</p>

 <h2>BODEGA ECOLÓGICA:</h2>

 <div class="empresa-textos">
 <p><strong>Nuestro emprendimiento familiar se inició hace dos décadas con la construcción de una bodega totalmente subterránea</strong> diseñada para producir vinos de alta gama, combinando la antigua tradición con la más avanzada tecnología, a partir de mantener la temperatura del vino y el ambiente que lo contiene idealmente constante sin         el uso de energía externa. 
    Está totalmente integrada desde la plantación de las vides hasta el producto terminado. Esta operada por 10 empleados que trabajan mano a mano con los dueños.</p>
 </div>

 <h2> NUESTRA MISIÓN:</h2>

 <div class="empresa-textos">
 <p><strong>"Ser altamente sostenibles reduciendo, reciclando, reusando y cuidando tanto al medio ambiente como a empleados y clientes”</strong></p>
 </div>


  <h2> NUESTROS VALORES:</h2>

 <div class="empresa-textos">
 <p>REDUCIR <br> 
    Todo el proceso de elaboración está 6m bajo tierra para <strong>mantener la temperatura gastando un 85% menos de energía</strong> que las demás bodegas.<br>
    <strong>Utilizar al máximo posible la iluminación natural</strong>. En complemento, luces de bajo consumo o fluorescentes en oficinas y salón de visitas y luces de sodio para las zonas de producción.
    Preferir el ingreso de <strong>uva de nuestra finca y de productores vecinos para reducir el transporte</strong>, y usar insumos más ecológicos especialmente <strong>botellas de vidrio livianas para reducir el impacto de la huella de carbono</strong>.<br>
    <strong>Política contra el uso de papel:</strong> no se hace ni publicidad ni marketing ni merchandising, ni folletos ni carteles. <strong>Se informa y publicita personalmente o via web.</strong></p>
 </div>


<h2> REUSAR Y RECICLAR</h2>

 <div class="empresa-textos">
 <p><strong>El agua</strong> de limpieza y enjuague de los procesos de producción <strong>se reutiliza para irrigación y los efluentes</strong> de cocinas y servicios son <strong>biodegradados naturalmente</strong>.<br>
    La mayor parte de los subproductos de la cosecha se reusan para otros productos como destilados y productos químicos o para mejorar la calidad de la tierra.<br>
    Las maderas del proceso también se reutilizan para muebles y accesorios que se comercializan in situ.<br>
    Las cajas de cartón se reutilizan para estibar botellas antes de embalarlas según necesidades de cada cliente.<br>
    <strong>Décadas de programas de reciclado y racionalización del uso de insumos para vidrio, plástico y cartón</strong>
    Se reutilizan los papeles ya impresos  en toda la empresa.<br>
    <strong>Los residuos orgánicos se seleccionan y se utilizan como compost para reutilizar en los parques y viñedos</strong>.</p>
 </div>


 <h2> PERSONAL Y CLIENTES</h2>

 <div class="empresa-textos">
 <p>Gran parte del personal (más del 50%) destina parte de su tiempo a <strong>educar a todos los visitantes sobre las características ecológicas de la empresa</strong>, como así también a los niños de escuelas y grupos de tercera edad.<br>
    Se prioriza el trabajo dentro de la empresa con los empleados. Se motivan y acompañan los emprendimientos propios del personal dentro y fuera de la empresa.<br>
    <strong>Tenemos una línea de vinos Eco-Estate que describe nuestra filosofía ecológica</strong> al público y se la describe en todas las contraetiquetas.<br>
    Transparencia: todas las contraetiquetas tienen el número de botella y lote para trazabilidad y además figura el correo electrónico del presidente de la empresa.<br>
